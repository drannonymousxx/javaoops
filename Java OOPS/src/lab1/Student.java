class Student {
    int roll;
    String name;
    double cgpa;
    public Student(int roll, String name, double cgpa) {
        this.roll = roll;
        this.name = name;
        this.cgpa = cgpa;
    }
    public String toString() {
        return "Roll: " + roll + ", Name: " + name + ", CGPA: " + cgpa;
    }
}




